''' 
입력 4일때 -> 0000, 0011, 1001, 1100, 1111   ->5출력
입력 5일때 -> 00001, 10000, 00100, 00111, 11111, 10011, 11001, 11100  -> 8출력
입력 6일때 -> 001100, 100100, 001111, 001001, 100111, 110011, 111001, 111100, 000000, 111111, 000011, 100001, 110000

피보나치 수열이 생각남 1 1 2 3 5 8 13 ...

f(n) = f(n-1) + f(n-2)
'''

import sys
input = sys.stdin.readline

n = int(input())
dp = [0] * 1000001
dp[1] = 1
dp[2] = 2  # 1+1

for i in range(3, n+1):
    dp[i] = (dp[i-1] + dp[i-2])%15746
print(dp[i])
